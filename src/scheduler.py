# ================================
# 📌 Codexへ指示：加工スケジューラーの実装
# ================================
# ■ 目的：
# 工程順制約・納期制約・夜間制限・分割禁止などを考慮した
# 「現実的かつ実用的な加工スケジュール」を生成すること。

# ■ 実装対象関数：
# def schedule_jobs(input_data): を再定義（下記条件を満たす）

# ■ 必須制約：
# - 工程は「前工程の完了数」までしか進められない
# - 各工程は「分割禁止」かつ「連続時間枠」での配置が必要
# - 各工程に「納期」が設定されており、期日までに最終工程完了が必須
# - 夜間加工は1日あたり6パレットまで
# - 作業時間は「日中」「夜間」の2区分があり、日別に時間が変動
# - 1工程ごとに「製品名、工程番号、必要時間、パレット数、納期」を持つ

# ■ 入力形式例（JSON）：
# {
#   "start_date": "2025-06-20",
#   "products": [
#     {"name": "A", "processes": [3, 4], "due": "2025-06-24", "count": 1},
#     {"name": "B", "processes": [2, 2], "due": "2025-06-22", "count": 1},
#     {"name": "C", "processes": [6], "due": "2025-06-23", "count": 1},
#     {"name": "D", "processes": [5], "due": "2025-06-25", "count": 1},
#     {"name": "E", "processes": [1, 1], "due": "2025-06-21", "count": 1}
#   ],
#   "work_schedule": {
#     "2025-06-20": {"day": 8, "night": 6},
#     "2025-06-21": {"day": 0, "night": 12},
#     "2025-06-22": {"day": 4, "night": 10},
#     "2025-06-23": {"day": 8, "night": 4},
#     "2025-06-24": {"day": 6, "night": 6},
#     "2025-06-25": {"day": 8, "night": 6}
#   }
# }

# ■ 出力形式例（JSON）：
# {
#   "schedule": [
#     {"date": "2025-06-20", "day": ["A 工程1 3h"], "night": ["B 工程1 2h"]},
#     {"date": "2025-06-21", "night": ["A 工程2 4h"]}
#   ]
# }

# ■ 実装の方針：
# 1. 製品ごとの工程をflattenして管理（順序付き）
# 2. 作業日ごとに日中・夜間スロットを保持し、連続時間確保できる枠を探索
# 3. 夜間優先配置（長時間加工は可能な限り夜間）
# 4. 各工程の納期チェック・前工程完了チェックを確実に行う
# 5. 工程は1単位で連続時間確保できた場合にのみ配置（分割禁止）

# ■ 注意：
# ロジックの見通しと保守性を重視し、関数分割・日付順処理を厳守すること。
